    private void preencheTable(ArrayList<Cliente> clientes) {
       DefaultTableModel dftm =(DefaultTableModel) jtCliente.getModel();
       
       while(dftm.getRowCount()>0){
           dftm.removeRow(0);
       }
       
       for(Cliente c: clientes){
           String[] linha= {c.getId() + "",c.getNome(), c.getCpf(),
               c.getLogin(), c.getSenha()};
           dftm.addRow(linha);
       }
    }
    private ArrayList<Cliente> clientes;
    
