    private void jbtnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnApagarActionPerformed
        int row = jtCliente.getSelectedRow();
        if(row<= -1){
            JOptionPane.showMessageDialog(null, "Selecione um cliente!");
        }else{
            String nome = clientes.get(row).getNome();
            if(JOptionPane.showConfirmDialog(null,
                    "Deseja apagar o cliente "+nome+" ?","Apagar cliente",
                JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
                try {
                    new ClienteDAO().apagar(clientes.get(row).getId());
                    JOptionPane.showMessageDialog(null, "Apagou!");
                    formWindowOpened(null);
                } catch (SQLException | ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possível apagar o cliente :(");

                }

            }
        }
    }//GEN-LAST:event_jbtnApagarActionPerformed

