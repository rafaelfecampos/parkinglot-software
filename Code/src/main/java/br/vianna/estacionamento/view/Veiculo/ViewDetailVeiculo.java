    private void jbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarActionPerformed
        try {
           
            // TODO add your handling code here:
            Veiculo vei = new Veiculo(cliDAO.buscarPorCpf(jtfCpfDono.getText()),
                    jtfModelo.getText(), (ETipoPlano) jcbPlano.getSelectedItem(),
                    jtfPlaca.getText());
            if(veiculo == null){
                new VeiculoDAO().inserir(vei);
                veiculos.add(vei);
            }else{
                new VeiculoDAO().alterar(vei);
                attVeiculo(vei);
            }
            
            JOptionPane.showMessageDialog(null, "Veículo salvo com sucesso :)");
            
            setVisible(false);
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível buscar o veículo :(");
        }

    }//GEN-LAST:event_jbtnSalvarActionPerformed

