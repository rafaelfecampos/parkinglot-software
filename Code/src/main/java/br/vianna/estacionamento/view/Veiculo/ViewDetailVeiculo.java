    private void preencheComboBox(ETipoPlano[] e) {
        DefaultComboBoxModel dcb =  (DefaultComboBoxModel) jcbPlano.getModel();
        dcb.removeAllElements();
        for(ETipoPlano tp : e){
            dcb.addElement(tp);
        }
        if(veiculo != null){
            jcbPlano.setSelectedItem(veiculo.getPlano());
        }
    }
    private ClienteDAO cliDAO = new ClienteDAO();
    private ArrayList<Veiculo> veiculos = new ArrayList<>();
    private Veiculo veiculo;
    
    public void preparedEdit(Veiculo vei){
        veiculo = vei;
        jtfModelo.setText(vei.getModelo());
        jtfPlaca.setText(vei.getPlaca());
        jtfCpfDono.setText(vei.getDono().getCpf());
        
    }
    public void attVeiculo(Veiculo vei) {
        for (Veiculo v :
                veiculos) {
            if (vei.getPlaca().equals(v.getPlaca())) {
                v.setModelo(vei.getModelo());
                v.setPlano(v.getPlano());
                v.setDono(v.getDono());
            }
            }
    }
    public ArrayList<Veiculo> getVeiculos(){
        return veiculos;
    }
