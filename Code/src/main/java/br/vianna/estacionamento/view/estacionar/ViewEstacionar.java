    private void jbtnNovaEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNovaEntradaActionPerformed
        int row = jtEstacionar.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um ve√≠culo!");
        } else {
            try {
                String placa = veiculos.get(row).getPlaca();
                Veiculo v = new VeiculoDAO().buscarPelaPlaca(veiculos.get(row).getPlaca());

                if (JOptionPane.showConfirmDialog(null, "Deseja dar entrada no ve√≠culo de placa:  " + 
                        placa + "?", "Dar entrada", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    LocalDateTime ld = LocalDateTime.now();
                    Estacionar est = new Estacionar(ld, v, 0);
                    EstacionarDAO estDAO = new EstacionarDAO();
                    estDAO.inserir(est);
                    est = estDAO.buscarPelaChaveSemSaida(row);
                    v.setId_estacionar(estDAO.buscarIdSemSaida(placa));
                    
                    

                    JOptionPane.showMessageDialog(null, "Entrada registrada para o ve√≠culo de placa: " + placa);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel conectar ao banco :(");

            }
        }
    }//GEN-LAST:event_jbtnNovaEntradaActionPerformed

    private void jbtnNovaSaidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnNovaSaidaActionPerformed
        // TODO add your handling code here:
        int row = jtEstacionar.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um ve√≠culo!");
        } else {
            try {
                String placa = veiculos.get(row).getPlaca();
                Veiculo v = new VeiculoDAO().buscarPelaPlaca(veiculos.get(row).getPlaca());


                if (JOptionPane.showConfirmDialog(null, "Deseja dar sa√≠da no ve√≠culo de placa:  " + 
                        placa + "?", "Dar sa√≠da", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    EstacionarDAO estDAO = new EstacionarDAO();
                    v.setId_estacionar(estDAO.buscarIdSemSaida(placa));
                    Estacionar est = estDAO.buscarPelaChaveSemSaida(v.getId_estacionar());
                    est.registrarSaida();
                    estDAO.alterar(est);
                    

                    JOptionPane.showMessageDialog(null, "Sa√≠da registrada para o ve√≠culo de placa: " + placa);
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "N√£o foi poss√≠vel conectar ao banco :(");

            }
        }
    }//GEN-LAST:event_jbtnNovaSaidaActionPerformed

