    private void preencheTable(ArrayList<Veiculo> veiculos) {
        DefaultTableModel dftm = (DefaultTableModel) jtEstacionar.getModel();

        while (dftm.getRowCount() > 0) {
            dftm.removeRow(0);
        }
        for (Veiculo v : veiculos) {
            String[] linha = {v.getModelo(), v.getPlano() + "", v.getPlaca(), v.getDono().getId() + ""};
            dftm.addRow(linha);
        }
    }

